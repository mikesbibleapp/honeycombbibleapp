#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import vm from "node:vm";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const indexPath = path.join(repoRoot, "index.html");
const outputPath = path.join(
  repoRoot,
  "supabase/functions/process-daily-surprises/verse-map.generated.ts",
);

function extractConst(source, name) {
  const marker = `const ${name} =`;
  const start = source.indexOf(marker);
  if (start < 0) throw new Error(`Could not find ${marker}`);
  let i = source.indexOf(name === "BIBLE" ? "{" : "[", start);
  if (i < 0) throw new Error(`Could not find ${name} initializer`);
  const open = source[i];
  const close = open === "{" ? "}" : "]";
  let depth = 0;
  let stringQuote = null;
  let escaped = false;
  for (; i < source.length; i += 1) {
    const ch = source[i];
    if (stringQuote) {
      if (escaped) escaped = false;
      else if (ch === "\\") escaped = true;
      else if (ch === stringQuote) stringQuote = null;
      continue;
    }
    if (ch === '"' || ch === "'" || ch === "`") {
      stringQuote = ch;
      continue;
    }
    if (ch === open) depth += 1;
    else if (ch === close) {
      depth -= 1;
      if (depth === 0) return source.slice(source.indexOf(open, start), i + 1);
    }
  }
  throw new Error(`Could not close ${name} initializer`);
}

const html = fs.readFileSync(indexPath, "utf8");
const BIBLE = vm.runInNewContext(`(${extractConst(html, "BIBLE")})`);
const PLAN = vm.runInNewContext(`(${extractConst(html, "PLAN")})`);

const byBook = new Map(BIBLE.books.map((book) => [book.name, book]));
const plan = PLAN.map(([book, chapter], index) => {
  const bookData = byBook.get(book);
  if (!bookData) throw new Error(`Plan references unknown book: ${book}`);
  const chapterData = bookData.chapters[chapter - 1];
  if (!chapterData) throw new Error(`Plan references missing chapter: ${book} ${chapter}`);
  return {
    index,
    book,
    chapter,
    verseCount: chapterData.length,
  };
});

const totalVerses = plan.reduce((sum, row) => sum + row.verseCount, 0);
const generated = `// Generated by scripts/generate-surprise-verse-map.mjs.
// Do not edit by hand. Source of truth is the BIBLE and PLAN constants in index.html.

export type SurpriseVersePlanEntry = {
  index: number;
  book: string;
  chapter: number;
  verseCount: number;
};

export const SURPRISE_VERSE_PLAN: SurpriseVersePlanEntry[] = ${JSON.stringify(plan)};

export const SURPRISE_VERSE_PLAN_STATS = {
  chapters: ${plan.length},
  verses: ${totalVerses},
};
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, generated);
console.log(
  `Wrote ${path.relative(repoRoot, outputPath)} (${plan.length} chapters, ${totalVerses} verses)`,
);
